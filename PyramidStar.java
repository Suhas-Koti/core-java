
public class PyramidStar {
    public static void main(String args[]) {
        int rows = 1;
        int columns = 4;

        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < columns; j++) {
                System.out.println("1 2 3 4");
            }
            System.out.println("");

        }
    }
}
