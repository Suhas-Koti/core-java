public class Mobile {
    private String model;
    private double price;

    public void setModel(String model) {
        this.model = model;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getModel() {
        return this.model;
    }

    public double getPrice() {
        return this.price;
    }
}
