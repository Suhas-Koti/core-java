public class Car {
    private String name;
    private String fuelType;

    public void setName(String name) {
        this.name = name;
    }

    public void setFuelType(String fuelType) {
        this.fuelType = fuelType;
    }

    public String getName() {
        return this.name;
    }

    public String getFuelType() {
        return this.fuelType;
    }
}
